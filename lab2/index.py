import turtle

L = 75

d0=[(L, 180), (2 * L, 90), (L, 90), (2 * L, 90), (0, -90)]
d1=[(1.41 * L, -135), (1.41 * L, 180), (2 * L, -135), (2 * L, 180), (0,-90)]
d2=[(L, 180), (L, 180), (L, -90), (1.41 * L,-45),(L, 135), (- L, 0), (-1.41 * L, -135), (- L, 45), (0, 90)]
d3=[(L, 180), (L, 180), (1.41 * L, -135), (L, 135), (1.41 * L, -135), (-1.41 * L, 0), (- L, 135), (1.41 * L, 45), (0, -45)]
d4=[(L, -90), (L, -90), (L, -90), (-L, 0), (L, -90), (L, -90), (2 * L, 180), (0, -90)]
d5=[(L, 180), (L, 90), (L, 90), (L, -90), (L, -90), (L, 180), (L, 90), (L, 90), (L, -90), (L, -90)]
d6=[(1.41 * L, -135), (L, 45), (L, 90), (L, 90), (L, 90), (1.41 * L, -135), (0, -45)]
d7=[(L, 180), (L, 180), (1.41 * L, -135), (L, 45), (-L, 0), (-1.41 * L, -45), (0, 135)]
d8=[(L, 180), (L, 90), (L, 90), (L, -90), (L, -90), (L, -90), (L, -90), (L, 90), (0, -90)]
d9=[(L, 180), (L, 90), (L, 90), (1.41 * L, -135), (-1.41 * L, 0), (L, -135), (0, -90)]
d=[d0, d1, d2, d3, d4, d5, d6, d7, d8, d9]

def twrite(n):
    turtle.pendown()
    for length, angle in d[n]:
        turtle.left(angle)
        turtle.forward(length)
    turtle.penup()
    turtle.forward(1.2 * L)

turtle.penup()
turtle.goto(-350, 0)

inp = open('input.txt', 'r')
number= inp.read()
inp.close()

for i in range (len(number)-1):
    twrite(int(number[i]))
